use rand::rngs::ThreadRng;
use rand::seq::SliceRandom;

/// Represents the different parts that can make up a cute ID
#[derive(Clone)]
pub enum IdPart {
    /// An animal name from a predefined list
    Animal,
    /// A color name from a predefined list
    Color,
    /// An adjective from a predefined list
    Adjective,
    /// An adverb from a predefined list
    Adverb,
}

/// Generator for creating cute, readable IDs by combining random words
pub struct CuteIdGenerator {
    /// Random number generator
    rng: ThreadRng,
    /// The ordered parts that will make up the ID
    parts: Vec<IdPart>,
    /// String used to join the parts together
    separator: String,
}

impl CuteIdGenerator {
    /// Creates a new CuteIdGenerator with the specified parts and separator
    ///
    /// # Arguments
    /// * `parts` - Vector of IdParts that defines the structure of generated IDs
    /// * `separator` - String used to join the parts together
    pub fn new(parts: Vec<IdPart>, separator: impl Into<String>) -> Self {
        Self {
            rng: rand::thread_rng(),
            parts,
            separator: separator.into(),
        }
    }

    /// Generates a new ID by randomly selecting words for each part
    ///
    /// # Returns
    /// A String containing the generated ID with parts joined by the separator
    pub fn generate(&mut self) -> String {
        let mut id_parts = Vec::new();

        for part in &self.parts {
            match part {
                IdPart::Animal => {
                    if let Some(animal) = ANIMALS.choose(&mut self.rng) {
                        id_parts.push(*animal);
                    }
                }
                IdPart::Color => {
                    if let Some(color) = COLORS.choose(&mut self.rng) {
                        id_parts.push(*color);
                    }
                }
                IdPart::Adjective => {
                    if let Some(adjective) = ADJECTIVES.choose(&mut self.rng) {
                        id_parts.push(*adjective);
                    }
                }
                IdPart::Adverb => {
                    if let Some(adverb) = ADVERBS.choose(&mut self.rng) {
                        id_parts.push(*adverb);
                    }
                }
            }
        }

        id_parts.reverse();

        id_parts.join(&self.separator)
    }
}

impl Default for CuteIdGenerator {
    /// Creates a default CuteIdGenerator that generates IDs in the format:
    /// "adjective-color-animal"
    fn default() -> Self {
        Self::new(vec![IdPart::Adjective, IdPart::Color, IdPart::Animal], "-")
    }
}

